let tbtn1 = document.getElementById("btn1")
let trans1 = document.getElementById("transcript1")
let isT1Displayed = false
tbtn1.addEventListener("click", function () {
    if (isT1Displayed) {
        trans1.style.display = "none"
        isT1Displayed = false
    } else {
        trans1.style.display = "block"
        isT1Displayed = true
    }
})

